async def main(drive_only=False):
    t0 = time.monotonic()
    if not drive_only:
        print("\\n=== STEP 1: Collections ===")
        create_or_get(BASE_COLLECTION)
        for name, parent_name in COLLECTION_ORDER:
            parent_key = STATE["collections"].get(parent_name or BASE_COLLECTION, "")
            create_or_get(name, parent_key)
        with open(STATE_FILE, "w") as f: json.dump(STATE, f, indent=2)
    print(f"\\n=== STEP 2: {{len(PAPERS)}} papers (concurrency={{CONCURRENCY}}, drive_only={{drive_only}}) ===")
    await asyncio.gather(*[
        process_paper(rel, doi, coll, tags, drive_only=drive_only)
        for rel, doi, coll, tags in PAPERS
    ])
    print(f"\\n=== STEP 3: {{len(FLAGGED)}} flagged items ===")
    await asyncio.gather(*[
        process_flagged(rel, query, coll, tags, drive_only=drive_only)
        for rel, query, coll, tags in FLAGGED
    ])
    print(f"\\n=== STEP 4: {{len(BOOKS)}} books/notes ===")
    await asyncio.gather(*[
        process_book(rel, itype, title, first, last, year, extra, coll, tags, drive_only=drive_only)
        for rel, itype, title, first, last, year, extra, coll, tags in BOOKS
    ])
    done    = sum(1 for v in STATE["papers"].values() if v.get("done"))
    errors  = [k for k, v in STATE["papers"].items() if not v.get("done")]
    elapsed = time.monotonic() - t0
    total   = len(PAPERS) + len(FLAGGED) + len(BOOKS)
    print(f"\\n=== DONE: {{done}}/{{total}} in {{elapsed:.0f}}s ===")
    if errors:
        print(f"Errors ({{len(errors)}}):")
        for e in errors: print(f"  {{e}}")

if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--mode", choices=["full", "drive-only"], default="full")
    a = parser.parse_args()
    asyncio.run(main(drive_only=(a.mode == "drive-only")))
